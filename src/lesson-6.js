const countryResult = hotels.reduce((countriesPerCities, item) => {
  // define var result with merged previous result
  const result = {
    ...countriesPerCities,
  };
  // if no country in result object
  if (!(item.country in result)) {
    // add new key with empty cities array
    result[item.country] = [];
  }
  // add current country city into country's cities array
  const currentCountryCities = result[item.country];
  // add current item's city
  currentCountryCities.push(item.city);
  // return new object with added country city
  return result;
}, {});

